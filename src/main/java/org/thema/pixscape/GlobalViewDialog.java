    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        startZ = Double.parseDouble(zEyeTextField.getText());
        direct = directCheckBox.isSelected();
        if(fromList.getModel().getSize() > 0) {
            from = new TreeSet<>(fromList.getSelectedValuesList());
            to = new TreeSet<>(toList.getSelectedValuesList());
            if(from.isEmpty() || to.isEmpty()) {
                JOptionPane.showMessageDialog(this, "At least one element must be selected in each list");
                return;
            }
        }
        aggr = noneRadioButton.isSelected() ? Aggregate.NONE : sumRadioButton.isSelected() ? Aggregate.SUM : Aggregate.SHANNON;
        if(bounds == null)
            bounds = new Bounds();
        sample = (int) sampleSpinner.getValue();
        anglePrec = Double.parseDouble(precTextField.getText()) * Math.PI / 180;
        
        isOk = true;
        doClose();
    }//GEN-LAST:event_okButtonActionPerformed

